# შემდეგ კი ის თანხა, რომელიც სასურველი ნივთის საყიდლად სჭირდება. დაბეჭდეთ შეუძლია ყიდვა თუ არა.

budget = float(input("შეიყვანეთ თქვენი ბიუჯეტი: "))

cost = float(input("შეიყვანეთ ნივთის ღირებულება: "))

if budget >= cost:
    print("თქვენ შეგიძლიათ შეიძინოთ ეს ნივთი.")
else:
    print("თქვენ არ გაქვთ საკმარისი თანხა ნივთის შესაძენად.")
